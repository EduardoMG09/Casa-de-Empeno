    private void ACTIONCALCULAR(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ACTIONCALCULAR
        // TODO add your handling code here: 
        CasaEmpeño prendax;
        prendax = new CasaEmpeño();
        prendax.setPagofinal(Double.parseDouble(PRESTADO3.getText()));
        double adicion20 = (Double.parseDouble(PRESTADO3.getText())*.40);
        adicion20 = adicion20 / 0.60;
        adicion20 = Double.parseDouble(PRESTADO3.getText())+adicion20;
        adicion20 = adicion20 * .20;
        prendax.setPagofinal(adicion20);
        prendaguardada[numeroPrendas]=prendax;
        
        int seleccionada = LISTA3.getSelectedIndex();
        String [] parts = ListaPrendas[seleccionada].getFecha().split(" ");
        int DIAS1 = Integer.parseInt(parts[0]);
        int MES1 = Integer.parseInt(parts[1]);
        
        String fecha2 = DIA2.getText()+" "+MES2.getText()+" "+AÑO2.getText();
        String [] parts2 = fecha2.split(" ");
        int DIAS2 = Integer.parseInt(parts2[0]);
        int MES2 = Integer.parseInt(parts2[1]);
        
        int DIASTOTALES=0;
        if(MES2 == MES1){
            DIASTOTALES = DIAS2-DIAS1;
            int diferencia = (30-DIAS1)+DIAS2;
            if(DIASTOTALES > 30){
                for(int i=0;i<= diferencia;i++){
                    prendax.setPagofinal(ListaPrendas[seleccionada].getPrestamo()*.03);
                }
            }
        }
        else if(MES2 != MES1){
            DIASTOTALES = (30-DIAS1)+DIAS2; 
            if(DIASTOTALES > 30){
                int diferencia = DIASTOTALES - 30;
                for(int i=0;i <= diferencia;i++){
                    prendax.setPagofinal(ListaPrendas[seleccionada].getPrestamo()*.03);
                }
            }
            
        }
        REMBOLSO.setText(String.valueOf(prendax.getPagofinal()));
        numeroPrendas++;
    }//GEN-LAST:event_ACTIONCALCULAR

