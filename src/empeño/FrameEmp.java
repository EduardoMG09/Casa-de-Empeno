    private void LISTA2(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LISTA2
        // TODO add your handling code here:
        int seleccionado = LISTA2.getSelectedIndex();
        //String FechaR = DAY.getText().toString()+"/" + MONTH.getText().toString()+"/"+ YEAR.getText().toString();

        PRENDA.setText(ListaPrendas[seleccionado].getPrenda());
        String [] parts =  ListaPrendas[seleccionado].getFecha().split(" ");
        DAY.setText(parts[0]);
        MONTH.setText(parts[1]);
        YEAR.setText(parts[2]);
        VALUATE.setText(String.valueOf(ListaPrendas[seleccionado].getValorComercial()));
        PRESTAMO.setText(String.valueOf(ListaPrendas[seleccionado].getPrestamo()));
        if(ListaPrendas[seleccionado].getCategoria()=="JOYERIA"){
            CATEGORIA.setSelectedIndex(0);
        }
        else if(ListaPrendas[seleccionado].getCategoria()=="ELECTRONICO"){
            CATEGORIA.setSelectedIndex(1);
        }
        else if(ListaPrendas[seleccionado].getCategoria()=="ELECTRODOMESTICO"){
            CATEGORIA.setSelectedIndex(2);
        }
        else if(ListaPrendas[seleccionado].getCategoria()=="JUGUETE"){
            CATEGORIA.setSelectedIndex(3);
        }
        else if(ListaPrendas[seleccionado].getCategoria()=="MUEBLE"){
            CATEGORIA.setSelectedIndex(4);
        }
    }//GEN-LAST:event_LISTA2

